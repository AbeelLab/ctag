package nl.tudelft.pl2.representation.graph;

/**
 * The directions in which the Graph or
 * the centre of the Graph may be zoomed.
 */
public enum ZoomDirection {
    /**
     * Zooming out, making more information
     * fit the screen.
     */
    OUT,

    /**
     * Zooming in. making less information
     * fit the screen.
     */
    IN
}
