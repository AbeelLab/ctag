package nl.tudelft.pl2.representation.graph;

/**
 * The directions in which the Graph or
 * the centre of the Graph may be moved.
 */
public enum MoveDirection {
    /**
     * Movement towards the right.
     */
    RIGHT,

    /**
     * Movement towards the left.
     */
    LEFT,

    /**
     * Movement upwards.
     */
    UP,

    /**
     * Movement downwards.
     */
    DOWN
}
